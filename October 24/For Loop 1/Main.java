import java.util.*;
import java.io.*;


public class Main {
    public static void main(String[] args) {
      
      for(int c = 1 ; c <= 10; c++){
        System.out.print(" " + c + " ");
      }
      System.out.println("");
      for(int c = 3 ; c <= 15; c+=2){
        System.out.print(" " + c + " ");
      }
      System.out.println("");

      for(int c = 10 ; c >= 1; c--){
        System.out.print(" " + c + " ");
      }
      System.out.println("");

      for(int c = 4 ; c <= 20; c+=4){
        System.out.print(" " + c + " ");
      }
      System.out.println("");

      for(int c = 125 ; c >= 0; c-=25){
        System.out.print(" " + c + " ");
      }


        
  }
}